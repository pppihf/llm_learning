print("""
Q1: VLM 是怎么让 LLM 看懂图片的？

A: 核心是三步走：
   (1) 用预训练的 Vision Encoder (如 ViT) 把图像编码成向量序列
   (2) 用 Projector (MLP) 把视觉向量映射到 LLM 的 embedding 空间
   (3) 视觉 token 和文本 token 拼接在一起，由 LLM 统一做 Self-Attention
   这样 LLM 处理视觉信息就像处理"翻译过来的文本"一样。


Q2: 为什么微调 VLM 时一般冻结 ViT？

A: 三个原因:
   (1) ViT 在大规模数据上预训练过，视觉特征已经足够好
   (2) 冻结 ViT 可以大幅减少需要训练的参数和显存
   (3) 如果微调数据量不大，解冻 ViT 可能导致视觉能力退化
   例外：如果下游任务的视觉域和预训练差异很大（如医疗影像），
   可以考虑解冻 ViT。


Q3: VLM 训练时显存波动大是为什么？

A: 因为不同图片的分辨率不同 → 编码后的视觉 token 数不同
   → 送入 LLM 的序列长度不同 → 激活值显存不同。
   例如 224×224 图产生 64 个视觉 token，
   而 1344×1344 图产生 2304 个 token，差 36 倍。
   解决方案: 限制 IMAGE_MAX_TOKEN_NUM 和 max_length。


Q4: eval_loss 和 train_loss 有什么区别？什么时候该停？

A: train_loss 是训练集上的损失，每个 step 更新；
   eval_loss 是验证集上的损失，每隔 N 步评估一次。
   两者关系:
   - 都在降: 正常训练中 ✅
   - train 降 eval 升: 过拟合 ⚠️ → 该停了
   - train 不降: 学习率/数据/架构有问题 ❌
   我们的训练中两者同步下降且 eval_loss 后期减速 → 合理停止。


Q5: LoRA 微调 VLM 和全量微调相比，有什么优缺点？

A: LoRA 优点:
   - 显存小 (rank=16 只训练 ~33M vs 8B)
   - 训练快
   - 天然抗灾难性遗忘 (原始权重不动)
   - 多个 LoRA adapter 可以热切换
   
   LoRA 缺点:
   - 表达能力有限，极端任务不如全量
   - rank 选择需要调参
   - 推理时如果不合并，有额外的矩阵运算开销

   实践中，LoRA 是 VLM 微调的首选方案。


Q6: 什么是多模态幻觉 (Hallucination)？怎么缓解？

A: 幻觉是 VLM 描述图片中不存在的物体或属性。例如:
   图片中没有猫，模型却说"图片中有一只猫"。

   缓解方法:
   (1) 高质量训练数据 (减少图文不匹配)
   (2) 多模态 RLHF (人类反馈强化学习)
   (3) 对比解码 (Contrastive Decoding): 对比有图和无图的输出
   (4) 训练时加入"否定样本" (图片中没有 X，回答没有)
   (5) 评估: 用 HallusionBench / POPE 等 benchmark 量化
""")


