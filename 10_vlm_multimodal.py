def calc_lora_params(hidden_dim, num_layers, rank, modules_per_layer=7):
    """
    估算 LoRA 参数量
    modules_per_layer: 每层有几个 linear (q,k,v,o,gate,up,down = 7)
    每个 LoRA: 2 × hidden_dim × rank
    """
    params_per_module = 2 * hidden_dim * rank  # A: dim×rank, B: rank×dim
    total_params = params_per_module * modules_per_layer * num_layers
    return total_params

# Qwen3-8B: hidden=4096, 36 layers, 7 linear per layer
